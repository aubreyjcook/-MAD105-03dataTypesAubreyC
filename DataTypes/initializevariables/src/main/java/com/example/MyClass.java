package com.example;

/* Program by Aubrey Cook
*
* This program is meant to demonstrate all the PRIMITIVE data types in Java and initialize them.
* */

public class MyClass
{
    public static void main(String[] args){

        byte byteExample = 0;
        short shortExample = 14;
        int intExample = 100000;
        long longExample = 50L;

        float floatExample = 0.99999f;
        double doubleExample = 0.12d;

        boolean boolExample = false;

        char charExample = 's';

        System.out.println(byteExample);
        System.out.println(shortExample);
        System.out.println(intExample);
        System.out.println(longExample);

        System.out.println(floatExample);
        System.out.println(doubleExample);

        System.out.println(boolExample);

        System.out.println(charExample);
    }
}
